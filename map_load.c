#include "wolf.h"

void	f_read(t_data *data)
{
	int		fd;
	char	line[1];
	int		i;
	int		j[2];

	fd = open("map", O_RDONLY);
	i = -1;
	j[0] = 0;
	j[1] = 0;
	while ((i = read(fd, line, 1)) > 0)
	{
		if (j[1] == 25)
		{
			j[1] = 0;
			j[0]++;
		}
		if (line[0] == '0')
		{
			data->map[j[0]][j[1]] = 0;
			j[1]++;
		}
		if (line[0] == '1')
		{
			data->map[j[0]][j[1]] = 1;
			j[1]++;
		}
		if (line[0] == '2')
		{
			data->map[j[0]][j[1]] = 2;
			j[1]++;
		}
		if (line[0] == '3')
		{
			data->map[j[0]][j[1]] = 3;
			j[1]++;
		}
	}
	close(fd);
}
